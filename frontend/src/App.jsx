function ScannerApp({ apiFetch }) {
  const [repoUrl, setRepoUrl] = useState(() => localStorage.getItem('last_repo_url') || '');
  const [githubToken, setGithubToken] = useState('');
  const [isScanning, setIsScanning] = useState(false);
  const [results, setResults] = useState(null);
  const [error, setError] = useState(null);
  const [filterSeverity, setFilterSeverity] = useState('all');
  const [sortBy, setSortBy] = useState('severity');
  const [scanStatus, setScanStatus] = useState({ detect: 'idle', scanners: 'idle', ai: 'idle' });
  const [scanComplete, setScanComplete] = useState(false);
  const [scanDuration, setScanDuration] = useState(null);
  const [formSticky, setFormSticky] = useState(false);
  const sentinelRef = useRef(null);
  const scanStartRef = useRef(null);

  useEffect(() => {
    const el = sentinelRef.current;
    if (!el) return;
    const obs = new IntersectionObserver(
      ([entry]) => setFormSticky(!entry.isIntersecting),
      { threshold: 0, rootMargin: '-80px 0px 0px 0px' }
    );
    obs.observe(el);
    return () => obs.disconnect();
  }, []);

  useEffect(() => {
    const handler = () => {
      setResults(null);
      setError(null);
      setScanStatus({ detect: 'idle', scanners: 'idle', ai: 'idle' });
      setScanDuration(null);
    };
    window.addEventListener('app:escape', handler);
    return () => window.removeEventListener('app:escape', handler);
  }, []);

  const handleRipple = (e) => {
    if (isScanning || !repoUrl) return;
    const btn = e.currentTarget;
    const rect = btn.getBoundingClientRect();
    const ripple = document.createElement('span');
    ripple.className = 'btn-ripple';
    ripple.style.left = `${e.clientX - rect.left}px`;
    ripple.style.top = `${e.clientY - rect.top}px`;
    btn.appendChild(ripple);
    setTimeout(() => ripple.remove(), 650);
  };

  const handleScan = async (e) => {
    e.preventDefault();
    if (!repoUrl) return;
    setIsScanning(true);
    setResults(null);
    setError(null);
    setFilterSeverity('all');
    setSortBy('severity');
    setScanComplete(false);
    setScanDuration(null);
    scanStartRef.current = Date.now();
    setScanStatus({ detect: 'scanning', scanners: 'idle', ai: 'idle' });
    try {
      const response = await apiFetch('/scan', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ repo_url: repoUrl, github_token: githubToken }),
      });
      if (!response.ok) { const e = await response.json(); throw new Error(e.detail || 'Scan failed'); }
      setScanStatus({ detect: 'done', scanners: 'scanning', ai: 'scanning' });
      const data = await response.json();
      localStorage.setItem('last_repo_url', repoUrl);
      setResults(data);
      setScanStatus({ detect: 'done', scanners: 'done', ai: 'done' });
      setScanComplete(true);
      setScanDuration(((Date.now() - scanStartRef.current) / 1000).toFixed(1));
      setTimeout(() => setScanComplete(false), 1800);
      // Celebrate a clean result
      const vulnCount = Object.keys(data.results || {})
        .filter(k => k !== 'ai_review')
        .reduce((sum, k) => sum + (data.results[k]?.vulnerabilities?.length || 0), 0);
      if (vulnCount === 0) {
        confetti({ particleCount: 130, spread: 80, origin: { y: 0.55 },
          colors: ['#58a6ff', '#d2a8ff', '#3fb950', '#ffa657', '#ffffff'] });
      }
    } catch (err) {
      setError(err.message);
      setScanStatus({ detect: 'error', scanners: 'error', ai: 'error' });
    } finally {
      setIsScanning(false);
    }
  };

  const getStatusIcon = (s) => {
    switch (s) {
      case 'idle':     return <Box />;
      case 'scanning': return <Loader2 className="loader" />;
      case 'done':     return <CheckCircle2 />;
      case 'error':    return <XCircle />;
      default:         return <Box />;
    }
  };

  const getAllVulnerabilities = () => {
    if (!results?.results) return [];
    return Object.keys(results.results)
      .filter(k => k !== 'ai_review')
      .flatMap(k => results.results[k]?.vulnerabilities || []);
  };

  const allVulns = getAllVulnerabilities();
  const filteredAndSorted = allVulns
    .filter(v => filterSeverity === 'all' || v.severity?.toLowerCase() === filterSeverity)
    .sort((a, b) => sortBy === 'severity'
      ? (SEVERITY_ORDER[a.severity?.toLowerCase()] ?? 4) - (SEVERITY_ORDER[b.severity?.toLowerCase()] ?? 4)
      : (a.package_name || '').localeCompare(b.package_name || ''));

  const metrics = {
    total:    allVulns.length,
    critical: allVulns.filter(v => v.severity?.toLowerCase() === 'critical').length,
    high:     allVulns.filter(v => v.severity?.toLowerCase() === 'high').length,
    medium:   allVulns.filter(v => v.severity?.toLowerCase() === 'medium').length,
    low:      allVulns.filter(v => v.severity?.toLowerCase() === 'low').length,
  };

  return (
    <div className="app-container">
      {scanComplete && <div className="scan-complete-overlay" />}
      <ScanProgressBar active={isScanning} />

      <header>
        <h1 className="logo"><ShieldCheck size={40} color="#58a6ff" />SecureScan</h1>
        <p className="subtitle">Universal Security Scanner for GitHub Repositories</p>
      </header>

      <div ref={sentinelRef} style={{ height: 1, marginBottom: '-1px' }} />
      <form className={`search-container${formSticky ? ' search-form-sticky' : ''}`} onSubmit={handleScan}>
        <div className="input-wrapper">
          <Code className="input-icon" size={20} />
          <input type="url" className="repo-input" placeholder="GitHub Repository URL"
            value={repoUrl} onChange={e => setRepoUrl(e.target.value)} disabled={isScanning} required />
        </div>
        <div className="input-wrapper" style={{ maxWidth: '200px' }}>
          <Key className="input-icon" size={20} />
          <input type="password" className="repo-input" placeholder="Token (Optional)"
            value={githubToken} onChange={e => setGithubToken(e.target.value)} disabled={isScanning} />
        </div>
        <button type="submit" className={`scan-btn ${isScanning ? 'scanning' : ''}`}
          disabled={isScanning || !repoUrl} onMouseDown={handleRipple}>
          {isScanning ? <><Loader2 size={20} className="loader" /> Scanning...</> : <><Search size={20} /> Scan Repo</>}
        </button>
      </form>

      {error && (
        <div className="dashboard" style={{ marginBottom: '2rem' }}>
          <div className="status-card" style={{ borderColor: 'rgba(255,123,114,0.3)' }}>
            <div className="status-icon-wrapper" style={{ color: '#ff7b72', background: 'rgba(255,123,114,0.1)' }}><AlertTriangle size={24} /></div>
            <div className="status-info"><h3>Error Occurred</h3><p style={{ color: '#ff7b72' }}>{error}</p></div>
          </div>
        </div>
      )}

      {(isScanning || results) && !error && (
        <div className="dashboard">
          <div className="status-grid">
            <div className="status-card">
              <div className={`status-icon-wrapper status-${scanStatus.detect}`}>{getStatusIcon(scanStatus.detect)}</div>
              <div className="status-info">
                <h3>Project Detection</h3>
                <p>{scanStatus.detect === 'done' ? `Detected: ${results?.project_types?.join(', ') || 'None'}` : 'Analyzing...'}</p>
              </div>
            </div>
            {results?.project_types?.map(type => {
              if (type === 'docker') return null;
              const sr = results.results?.[type];
              return (
                <div key={type} className="status-card">
                  <div className="status-icon-wrapper status-done"><Terminal /></div>
                  <div className="status-info">
                    <h3 style={{ textTransform: 'capitalize' }}>{type} Scanner</h3>
                    <p>{sr?.vulnerabilities?.length || 0} Issues Found</p>
                  </div>
                </div>
              );
            })}
            {isScanning && scanStatus.detect === 'done' && (
              <div className="status-card">
                <div className="status-icon-wrapper status-scanning"><Loader2 className="loader" /></div>
                <div className="status-info"><h3>Deep Scan</h3><p>Auditing dependencies...</p></div>
              </div>
            )}
            <div className="status-card">
              <div className={`status-icon-wrapper status-${scanStatus.ai}`}>{getStatusIcon(scanStatus.ai)}</div>
              <div className="status-info">
                <h3>AI Code Review</h3>
                <p>{scanStatus.ai === 'done'
                  ? (results?.results?.ai_review?.status === 'error' ? `Failed (${results?.results?.ai_review?.error || 'No API Key'})` : 'Completed')
                  : (scanStatus.ai === 'scanning' ? 'Analyzing...' : 'Pending')}
                </p>
              </div>
            </div>
          </div>

          {results && (
            <>
              {scanDuration && (
                <div className="scan-duration-badge">
                  <CheckCircle2 size={13} /> Completed in {scanDuration}s
                </div>
              )}
              <div className="metrics-bar">
                <SeverityDonut critical={metrics.critical} high={metrics.high} medium={metrics.medium} low={metrics.low} />
                {[
                  { val: metrics.total,    label: 'Total Issues', cls: 'value-total' },
                  { val: metrics.critical, label: 'Critical',     cls: 'value-critical' },
                  { val: metrics.high,     label: 'High',         cls: 'value-high' },
                  { val: metrics.medium,   label: 'Medium',       cls: 'value-medium' },
                ].map(({ val, label, cls }) => (
                  <div key={label} className="metric-card">
                    <div className={`metric-value ${cls}`}><AnimatedNumber value={val} /></div>
                    <div className="metric-label">{label}</div>
                  </div>
                ))}
              </div>

              <ExportBar repoUrl={repoUrl} results={results} allVulns={allVulns} />

              {allVulns.length > 0 && (
                <RevealCard className="results-section">
                  <h2 className="section-title">
                    <FileCode2 size={24} color="#58a6ff" />
                    <span className="section-title-text">Dependency Vulnerabilities</span>
                  </h2>
                  <VulnFilterBar allVulns={allVulns} filterSeverity={filterSeverity}
                    setFilterSeverity={setFilterSeverity} sortBy={sortBy} setSortBy={setSortBy} />
                  {filteredAndSorted.length === 0
                    ? <div className="vuln-empty">No {filterSeverity} vulnerabilities found.</div>
                    : <div className="vuln-grid">{filteredAndSorted.map((vuln, i) => <VulnCard key={i} vuln={vuln} />)}</div>}
                </RevealCard>
              )}

              {results.previous_reviews?.length > 0 && (
                <RevealCard className="results-section" delay={0.1}>
                  <h2 className="section-title">
                    <History size={24} color="#d2a8ff" />
                    <span className="section-title-text">Previous AI Reviews ({results.previous_reviews.length})</span>
                  </h2>
                  {results.previous_reviews.map((review, i) => (
                    <PreviousReviewItem key={i} review={review} index={i} total={results.previous_reviews.length} />
                  ))}
                </RevealCard>
              )}

              {results.results?.ai_review?.ai_output && (
                <RevealCard className="results-section" delay={0.15}>
                  <h2 className="section-title">
                    <Bot size={24} color="#d2a8ff" />
                    <span className="section-title-text">Claude AI Review — Latest</span>
                  </h2>
                  <div className="ai-review-card">
                    <AIMarkdown>{results.results.ai_review.ai_output}</AIMarkdown>
                  </div>
                </RevealCard>
              )}

              {results.results?.ai_review?.status === 'error' && (
                <RevealCard className="results-section" delay={0.15}>
                  <h2 className="section-title">
                    <Bot size={24} color="#ff7b72" />
                    <span className="section-title-text">Claude AI Review Failed</span>
                  </h2>
                  <div className="ai-review-card" style={{ borderColor: 'rgba(255,123,114,0.3)' }}>
                    <div className="ai-content" style={{ color: '#ff7b72' }}>
                      Error: {results.results.ai_review.error || 'Unknown error occurred during AI review.'}
                    </div>
                  </div>
                </RevealCard>
              )}
            </>
          )}
        </div>
      )}
    </div>
  );
}

// ─── Root ─────────────────────────────────────────────────────────────────────

function App() {
  const [credentials, setCredentials] = useState(() => sessionStorage.getItem('auth') || null);
  const [view, setView] = useState(() => new URLSearchParams(window.location.search).get('view') || 'scanner');
  const [toastMsg, setToastMsg] = useState(null);
  const [navHidden, setNavHidden] = useState(false);
  const toastTimer = useRef(null);
  const lastScrollY = useRef(0);
  const navigateRef = useRef(null);

  const handleLogin  = (creds) => { sessionStorage.setItem('auth', creds); setCredentials(creds); };
  const handleLogout = ()      => { sessionStorage.removeItem('auth');       setCredentials(null); setView('scanner'); };

  const navigate = (newView) => {
    setView(newView);
    window.history.pushState({}, '', newView === 'scanner' ? window.location.pathname : `?view=${newView}`);
  };
  navigateRef.current = navigate;

  const reScan = (url) => {
    localStorage.setItem('last_repo_url', url);
    navigate('scanner');
  };

  // Handle browser back/forward
  useEffect(() => {
    const onPop = () => setView(new URLSearchParams(window.location.search).get('view') || 'scanner');
    window.addEventListener('popstate', onPop);
    return () => window.removeEventListener('popstate', onPop);
  }, []);

  // Toast notification listener
  useEffect(() => {
    const handler = (e) => {
      if (toastTimer.current) clearTimeout(toastTimer.current);
      setToastMsg(e.detail.message);
      toastTimer.current = setTimeout(() => setToastMsg(null), 2200);
    };
    window.addEventListener('app:toast', handler);
    return () => window.removeEventListener('app:toast', handler);
  }, []);

  // Hide nav on scroll down, reveal on scroll up
  useEffect(() => {
    const onScroll = () => {
      const y = window.scrollY;
      const delta = y - lastScrollY.current;
      if (delta > 10 && y > 100) setNavHidden(true);
      else if (delta < -10) setNavHidden(false);
      lastScrollY.current = y;
    };
    window.addEventListener('scroll', onScroll, { passive: true });
    return () => window.removeEventListener('scroll', onScroll);
  }, []);

  // Keyboard shortcuts
  useEffect(() => {
    const handler = (e) => {
      if ((e.metaKey || e.ctrlKey) && e.key === 'k') {
        e.preventDefault();
        navigateRef.current('scanner');
        setTimeout(() => document.querySelector('.search-container .repo-input')?.focus(), 80);
      }
      if ((e.metaKey || e.ctrlKey) && e.key === 'Enter') {
        document.querySelector('.search-container')?.requestSubmit?.();
      }
      if (e.key === 'Escape') {
        window.dispatchEvent(new CustomEvent('app:escape'));
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, []);

  // Cursor spotlight + 3D tilt for cards (event delegation — no per-card hooks needed)
  useEffect(() => {
    const onMove = (e) => {
      const card = e.target.closest('.vuln-card, .metric-card, .status-card, .repo-history-block');
      if (!card) return;
      const r = card.getBoundingClientRect();
      const xPct = (e.clientX - r.left) / r.width;
      const yPct = (e.clientY - r.top) / r.height;
      card.style.setProperty('--mx', `${(xPct * 100).toFixed(1)}%`);
      card.style.setProperty('--my', `${(yPct * 100).toFixed(1)}%`);
      if (card.classList.contains('vuln-card')) {
        const rotX = ((yPct - 0.5) * -13).toFixed(2);
        const rotY = ((xPct - 0.5) * 13).toFixed(2);
        card.style.transform = `perspective(700px) rotateX(${rotX}deg) rotateY(${rotY}deg) scale(1.025) translateY(-2px)`;
      }
    };
    const onOut = (e) => {
      const card = e.target.closest('.vuln-card, .metric-card, .status-card, .repo-history-block');
      if (card && !card.contains(e.relatedTarget)) {
        card.style.removeProperty('--mx');
        card.style.removeProperty('--my');
        if (card.classList.contains('vuln-card')) card.style.transform = '';
      }
    };
    document.addEventListener('mousemove', onMove);
    document.addEventListener('mouseout', onOut);
    return () => {
      document.removeEventListener('mousemove', onMove);
      document.removeEventListener('mouseout', onOut);
    };
  }, []);

  const apiFetch = (path, options = {}) =>
    fetch(`${API_URL}${path}`, {
      ...options,
      headers: { ...(options.headers || {}), 'Authorization': `Basic ${credentials}` },
    }).then(res => {
      if (res.status === 401) { handleLogout(); throw new Error('Session expired. Please sign in again.'); }
      return res;
    });

  return (
    <>
      <AmbientBackground />
      <Toast message={toastMsg} />
      {!credentials
        ? <LoginPage onLogin={handleLogin} />
        : <>
            <NavBar view={view} onNavigate={navigate} onLogout={handleLogout} hidden={navHidden} />
            <div className="with-nav" key={view}>
              {view === 'history'  ? <HistoryView       apiFetch={apiFetch} onReScan={reScan} />
             : view === 'packages' ? <PackageHistoryView apiFetch={apiFetch} onReScan={reScan} />
             :                       <ScannerApp         apiFetch={apiFetch} />}
            </div>
          </>
      }
    </>
  );
}

export default App;
