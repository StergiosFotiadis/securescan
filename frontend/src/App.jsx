function formatTimestamp(iso) {
  try {
    return new Date(iso).toLocaleString(undefined, {
      year: 'numeric', month: 'short', day: 'numeric',
      hour: '2-digit', minute: '2-digit',
    });
  } catch { return iso; }
}

function vulnKey(v) { return v.cve_id || `${v.package_name}@${v.version}`; }

function computeDelta(prevScan, currScan) {
  const prevKeys = new Set((prevScan?.vulnerabilities || []).map(vulnKey));
  const currKeys = new Set((currScan?.vulnerabilities || []).map(vulnKey));
  return {
    newVulns:   currScan.vulnerabilities.filter(v => !prevKeys.has(vulnKey(v))),
    fixedVulns: (prevScan?.vulnerabilities || []).filter(v => !currKeys.has(vulnKey(v))),
  };
}

function getSeverityCounts(vulns) {
  return {
    critical: vulns.filter(v => v.severity?.toLowerCase() === 'critical').length,
    high:     vulns.filter(v => v.severity?.toLowerCase() === 'high').length,
    medium:   vulns.filter(v => v.severity?.toLowerCase() === 'medium').length,
    low:      vulns.filter(v => v.severity?.toLowerCase() === 'low').length,
  };
}

function generateMarkdown(repoUrl, results, allVulns) {
  const ts = new Date().toLocaleString();
  const projectTypes = results?.project_types?.join(', ') || 'Unknown';
  const counts = getSeverityCounts(allVulns);
  let md = `# SecureScan Report\n\n`;
  md += `**Repository:** ${repoUrl}  \n`;
  md += `**Scanned:** ${ts}  \n`;
  md += `**Project types:** ${projectTypes}\n\n`;
  md += `## Summary\n\n| Severity | Count |\n|---|---|\n`;
  md += `| 🔴 Critical | ${counts.critical} |\n`;
  md += `| 🟠 High | ${counts.high} |\n`;
  md += `| 🟣 Medium | ${counts.medium} |\n`;
  md += `| 🔵 Low | ${counts.low} |\n`;
  md += `| **Total** | **${allVulns.length}** |\n\n`;
  if (allVulns.length > 0) {
    md += `## Vulnerabilities\n\n`;
    const order = ['critical', 'high', 'medium', 'low'];
    const grouped = {};
    for (const v of allVulns) {
      const sev = v.severity?.toLowerCase() || 'unknown';
      (grouped[sev] = grouped[sev] || []).push(v);
    }
    for (const sev of order) {
      if (!grouped[sev]?.length) continue;
      md += `### ${sev.charAt(0).toUpperCase() + sev.slice(1)} (${grouped[sev].length})\n\n`;
      for (const v of grouped[sev]) {
        md += `#### ${v.package_name} v${v.version}\n`;
        if (v.cve_id) md += `- **CVE:** [${v.cve_id}](https://nvd.nist.gov/vuln/detail/${v.cve_id})\n`;
        md += `- **Scanner:** ${v.scanner_type || 'Unknown'}\n`;
        md += `- **Description:** ${v.description}\n\n`;
      }
    }
  } else {
    md += `## ✅ No vulnerabilities found\n\n`;
  }
  if (results?.results?.ai_review?.ai_output) {
    md += `## AI Code Review\n\n${results.results.ai_review.ai_output}\n\n`;
  }
  md += `---\n*Generated by SecureScan*\n`;
  return md;
}

function generateCSV(allVulns) {
  const headers = ['Package', 'Version', 'Severity', 'CVE ID', 'Scanner', 'Description'];
  const esc = (s) => `"${String(s || '').replace(/"/g, '""')}"`;
  const rows = allVulns.map(v =>
    [v.package_name, v.version, v.severity, v.cve_id || '', v.scanner_type || '', v.description].map(esc).join(',')
  );
  return [headers.map(esc).join(','), ...rows].join('\n');
}

function downloadFile(content, filename, mime) {
  const url = URL.createObjectURL(new Blob([content], { type: mime }));
  Object.assign(document.createElement('a'), { href: url, download: filename }).click();
  URL.revokeObjectURL(url);
}

